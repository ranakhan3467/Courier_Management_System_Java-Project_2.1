    private void MakeOrderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MakeOrderButtonActionPerformed
        // TODO add your handling code here:
        String op = OriginTextField.getText();
        String dp = DestinationTextField.getText();
        int si = IteamTypeSelectComboBox.getSelectedIndex();
        String il = LengthTextField.getText();
        String iw = WidthTextField.getText();
        String ih = HeightTextField.getText();
        String iwe = WeightComboBox.getText();
        int iq = QuantityUnitComboBox.getSelectedIndex();

        int opv=0, dpv=0, opdpv=0, ilv=0, iwv=0, ihv=0, iwev=0;

        if(op.length()>0)
            opv=1;
        if(dp.length()>0)
            dpv=1;
        if(!op.equals(dp))
            opdpv=1;
        if(il.length()>0 && !(il.equals("0")))
            ilv=1;
        if(iw.length()>0 && !(iw.equals("0")))
            iwv=1;
        if(ih.length()>0 && !(ih.equals("0")))
            ihv=1;
        if(iwe.length()>0 && !(iwe.equals("0")))
            iwev=1;

        if(opv==0)
            JOptionPane.showMessageDialog(null, "Origin place is empty!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(dpv==0)
            JOptionPane.showMessageDialog(null, "Destination place is empty!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(opdpv==0)
            JOptionPane.showMessageDialog(null, "Origin and Destination place is same!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(ilv==0)
            JOptionPane.showMessageDialog(null, "Length is empty or 0!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(iwv==0)
            JOptionPane.showMessageDialog(null, "Width is empty or 0!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(ihv==0)
            JOptionPane.showMessageDialog(null, "Height is empty or 0!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(iwev==0)
            JOptionPane.showMessageDialog(null, "Weight is empty or 0!", "Error", JOptionPane.ERROR_MESSAGE);
        else if(iq==0)
            JOptionPane.showMessageDialog(null, "Quantinty should at least 1!", "Error", JOptionPane.ERROR_MESSAGE);

        if(opv==1 && dpv==1 && ilv==1 && iwv==1 && ihv==1 && iwev==1 && opdpv==1 && iq>0){
            if(iq==1)
            jTextField12.setText("200");
            else if(iq==2)
            jTextField12.setText("230");
            else if(iq==3)
            jTextField12.setText("250");
            else if(iq==4)
            jTextField12.setText("270");
            else if(iq==5)
            jTextField12.setText("290");
            else if(iq>5)
            jTextField12.setText("300");
            

            jTabbedPane1.setSelectedIndex(2);

        }
    }//GEN-LAST:event_MakeOrderButtonActionPerformed

