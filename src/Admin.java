    private void AddEmployeeInfoButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddEmployeeInfoButton10ActionPerformed
        // TODO add your handling code here:
        // Add New Employee
        String  id = EmployeeIDTextField1.getText();
        String  n  = EmployeeNameTextField1.getText();
        String  un = EmployeeAddressTextField10.getText();
        String  ag = EmployeeAddressTextField8.getText();
        String  mn = EmployeeMobileNoTextField1.getText();
        String  em = EmployeeAddressTextField7.getText();
        String  ad = EmployeeAddressTextField9.getText();
        String  p  = EmployeeAddressTextField6.getText();
        String  g  = jComboBox1.getSelectedItem().toString();
        
        int idv=0, nv=0, unv=0, agv=0, mnv=0, emv=0, adv=0, pv=0, gv=0;
        
        if(!id.isEmpty()){
            idv = 1;
            jLabel8.setText("");
        }
        else
            jLabel8.setText("Enter a valid ID!");
        if(!n.isEmpty()){
            nv = 1;
            jLabel9.setText("");
        }
        else
            jLabel9.setText("Enter Employee Name!");
        if(!un.isEmpty()){
            unv = 1;
            jLabel10.setText("");
        }
        else
            jLabel10.setText("Enter a valid Username!");
        if(!ag.isEmpty()){
            agv = 1;
            jLabel15.setText("");
        }
        else
            jLabel15.setText("Age is Empty!");
        if(!mn.isEmpty()){
            mnv = 1;
            jLabel11.setText("");
        }
        else
            jLabel11.setText("Enter a valid Mobile No!");
        if(!em.isEmpty()){
            emv = 1;
            jLabel12.setText("");
        }
        else
            jLabel12.setText("Enter a valid Email!");
        if(!ad.isEmpty()){
            adv = 1;
            jLabel13.setText("");
        }
        else
            jLabel13.setText("Enter a valid Address!");
        if(!p.isEmpty()){
            pv = 1;
        jLabel14.setText("");
        }
        else
            jLabel14.setText("Enter Password!");
        
        if(idv==1 && nv==1 && unv==1 && agv==1 && mnv==1 && emv==1 && adv==1 && pv==1){
        try {
            String sql = "INSERT INTO EMPLOYEE_INFO (ID, NAME, USERNAME, MOBILENO, ADDRESS, GENDER, PASSWORD, EMAIL, AGE) VALUES(?,?,?,?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setString(1, id);
            ps.setString(2, n);
            ps.setString(3, un);
            ps.setString(4, mn);
            ps.setString(5, ad);
            ps.setString(6, g);
            ps.setString(7, p);
            ps.setString(8, em);
            ps.setString(9, ag);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Added successfully", "Info", JOptionPane.INFORMATION_MESSAGE);
            showEmployee();
            jTabbedPane1.setSelectedIndex(4);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Username already exit", "Info", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_AddEmployeeInfoButton10ActionPerformed

